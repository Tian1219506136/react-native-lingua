import type { LanguageCode, Lesson, LessonImageKey } from "@/types/learning";

type PracticeLessonInput = {
  description: string;
  expectedIntent: string;
  id: string;
  imageKey: LessonImageKey;
  languageCode: LanguageCode;
  order: number;
  phrase: string;
  phraseTranslation: string;
  prompt: string;
  term: string;
  termTranslation: string;
  title: string;
  unitId: string;
};

function createPracticeLesson({
  description,
  expectedIntent,
  id,
  imageKey,
  languageCode,
  order,
  phrase,
  phraseTranslation,
  prompt,
  term,
  termTranslation,
  title,
  unitId,
}: PracticeLessonInput): Lesson {
  return {
    id,
    unitId,
    languageCode,
    title,
    description,
    imageKey,
    level: "beginner",
    order,
    xpReward: order <= 3 ? 25 : 30,
    estimatedMinutes: order <= 3 ? 5 : 6,
    goals: [
      { id: `${id}-goal-1`, label: `Recognize ${term}` },
      { id: `${id}-goal-2`, label: `Use ${phrase} in a short reply` },
    ],
    vocabulary: [
      {
        id: `${id}-vocab-1`,
        term,
        translation: termTranslation,
        partOfSpeech: "phrase",
      },
    ],
    phrases: [
      {
        id: `${id}-phrase-1`,
        phrase,
        translation: phraseTranslation,
      },
    ],
    activities: [
      {
        id: `${id}-listen-1`,
        type: "listen",
        prompt: "Listen and choose the meaning.",
        audioText: term,
        correctAnswer: termTranslation,
        xp: 5,
      },
      {
        id: `${id}-chat-1`,
        type: "chat",
        prompt,
        starterMessage: phrase,
        expectedIntent,
        xp: 20,
      },
    ],
    aiTeacherPrompt: {
      persona: "You are a friendly language tutor for absolute beginners.",
      lessonBrief: `Practice ${title.toLowerCase()} with one useful phrase and one short response.`,
      speakingFocus: `Help the learner say ${phrase} clearly.`,
      correctionStyle: "Give one short correction and let the learner try again.",
      fallbackPrompt: `Offer the sentence frame: ${phrase}.`,
    },
  };
}

